-- 2. Переносим данные в реальную таблицу с точным сопоставлением по ID упражнений из текущих сессий
INSERT INTO GTB_sets (id_workout_exercise, set_number, weight, reps, duration_seconds, distance_meters)
SELECT 
    we.id_workout_exercise,
    ns.set_number,
    ns.weight,
    ns.reps,
    CASE WHEN we.is_bodyweight = 2 THEN 300 ELSE NULL END AS duration_seconds, -- 5 минут для кардио
    NULL AS distance_meters
FROM @NewSets ns
INNER JOIN GTB_workouts w ON CAST(w.started_at AS DATE) = ns.workout_date
INNER JOIN GTB_workout_exercises we ON we.workout_id = w.id_workout AND we.exercise_name = ns.exercise_name
WHERE we.id_user = 1;

COMMIT TRANSACTION;

-- Проверочный SELECT для контроля получившейся структуры
SELECT 
    CAST(w.started_at AS DATE) AS [Дата],
    we.exercise_name AS [Упражнение],
    s.set_number AS [Подход],
    s.weight AS [Вес],
    s.reps AS [Повторения]
FROM GTB_sets s
INNER JOIN GTB_workout_exercises we ON we.id_workout_exercise = s.id_workout_exercise
INNER JOIN GTB_workouts w ON w.id_workout = we.workout_id
ORDER BY [Дата], we.id_workout_exercise, s.set_number;